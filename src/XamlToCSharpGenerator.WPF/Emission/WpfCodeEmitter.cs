using System;
using System.Collections.Generic;
using System.Globalization;
using System.Text;
using XamlToCSharpGenerator.Core.Abstractions;
using XamlToCSharpGenerator.Core.Models;
using XamlToCSharpGenerator.Core.Parsing;

namespace XamlToCSharpGenerator.WPF.Emission;

/// <summary>
/// Phase 3 code emitter for WPF XAML.
///
/// Emits pure C# object graph construction (no BAML LoadComponent path).
/// </summary>
public sealed class WpfCodeEmitter : IXamlCodeEmitter
{
    private const string GeneratorVersion = "0.3.0";
    private const string GeneratorName = "XamlToCSharpGenerator.Generator.WPF";
    private const string WpfFrameworkId = "WPF";
    private static readonly MarkupExpressionParser MarkupParser = new();

    public (string HintName, string Source) Emit(ResolvedViewModel viewModel)
    {
        var doc = viewModel.Document;
        if (doc.ClassFullName is null)
        {
            return (string.Empty, string.Empty);
        }

        var source = BuildSource(viewModel, doc);
        var hintName = doc.ClassFullName.Replace('.', '_').Replace('+', '_') + ".wpf.g.cs";
        return (hintName, source);
    }

    private static string BuildSource(ResolvedViewModel viewModel, XamlDocumentModel doc)
    {
        var sb = new StringBuilder(1024);
        var ns = doc.ClassNamespace;
        var className = doc.ClassName;
        var modifier = viewModel.ClassModifier;
        var hasNs = !string.IsNullOrEmpty(ns);

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by " + GeneratorName + " " + GeneratorVersion);
        sb.AppendLine("#nullable disable");
        sb.AppendLine("#pragma warning disable CS0169, CS8618");
        sb.AppendLine();

        if (hasNs)
        {
            sb.AppendLine("namespace " + ns);
            sb.AppendLine("{");
        }

        var indent = hasNs ? "    " : string.Empty;
        var emitter = new GraphEmitter(viewModel, sb, indent + "    ");

        sb.AppendLine(indent + modifier + " partial class " + className);
        sb.AppendLine(indent + "{");

        foreach (var element in viewModel.NamedElements)
        {
            sb.AppendLine(emitter.MemberIndent + "#line " + element.Line.ToString(CultureInfo.InvariantCulture));
            sb.AppendLine(emitter.MemberIndent + "[global::System.Diagnostics.CodeAnalysis.SuppressMessage(\"Microsoft.Performance\", \"CA1823:AvoidUnusedPrivateFields\")]");
            sb.AppendLine(emitter.MemberIndent + element.FieldModifier + " " + QualifyType(element.TypeName) + " " + element.Name + ";");
            sb.AppendLine(emitter.MemberIndent + "#line default");
            sb.AppendLine();
        }

        sb.AppendLine(emitter.MemberIndent + "private int __WXSG_HOT_RELOAD__;");
        sb.AppendLine(emitter.MemberIndent + "private bool _contentLoaded;");
        sb.AppendLine();

        EmitInitializeComponent(emitter, viewModel);

        if (IsApplicationDefinition(viewModel))
        {
            var startupWindowType = ResolveStartupWindowType(viewModel, doc);
            if (startupWindowType is not null)
            {
                sb.AppendLine();
                EmitOnStartup(emitter, startupWindowType);
            }

            sb.AppendLine();
            EmitMain(emitter, doc);
        }

        sb.AppendLine(indent + "}");

        if (hasNs)
        {
            sb.AppendLine("}");
        }

        return sb.ToString();
    }

    private static void EmitInitializeComponent(GraphEmitter emitter, ResolvedViewModel viewModel)
    {
        var sb = emitter.Builder;
        var i = emitter.MemberIndent;

        sb.AppendLine(i + "[global::System.Diagnostics.DebuggerNonUserCode]");
        sb.AppendLine(i + "[global::System.CodeDom.Compiler.GeneratedCode(\"" + GeneratorName + "\", \"" + GeneratorVersion + "\")]");
        sb.AppendLine(i + "public void InitializeComponent()");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    if (_contentLoaded) return;");
        sb.AppendLine(i + "    _contentLoaded = true;");
        sb.AppendLine(i + "    __WXSG_HOT_RELOAD__++;");
        sb.AppendLine(i);
        sb.AppendLine(i + "    // Phase 3: pure C# object-graph construction (no BAML LoadComponent).");
        sb.AppendLine(i + "    __WXSG_BuildObjectGraph();");
        sb.AppendLine(i + "}");
        sb.AppendLine();

        sb.AppendLine(i + "private void __WXSG_BuildObjectGraph()");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    var __root = this;");
        emitter.EmitNodeInitialization(viewModel.RootObject, "__root", isRootNode: true);
        sb.AppendLine(i + "}");
        sb.AppendLine();

        sb.AppendLine(i + "internal bool __WXSG_ApplyHotReload(string __filePath, string __xamlText)");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    _ = __filePath;");
        sb.AppendLine(i + "    _ = __xamlText;");
        sb.AppendLine(i + "    __WXSG_ResetForHotReload();");
        sb.AppendLine(i + "    __WXSG_HOT_RELOAD__++;");
        sb.AppendLine(i + "    _contentLoaded = true;");
        sb.AppendLine(i + "    __WXSG_BuildObjectGraph();");
        sb.AppendLine(i + "    return true;");
        sb.AppendLine(i + "}");
        sb.AppendLine();

        EmitHotReloadReset(emitter, viewModel);
    }

    private static void EmitHotReloadReset(GraphEmitter emitter, ResolvedViewModel viewModel)
    {
        var sb = emitter.Builder;
        var i = emitter.MemberIndent;

        sb.AppendLine(i + "private void __WXSG_ResetForHotReload()");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    if (__WXSG_HOT_RELOAD__ <= 0)");
        sb.AppendLine(i + "    {");
        sb.AppendLine(i + "        return;");
        sb.AppendLine(i + "    }");
        sb.AppendLine();
        sb.AppendLine(i + "    _contentLoaded = false;");

        if (viewModel.NamedElements.Length > 0)
        {
            sb.AppendLine();
            foreach (var namedElement in viewModel.NamedElements)
            {
                sb.AppendLine(i + "    this." + namedElement.Name + " = null;");
            }
        }

        emitter.EmitHotReloadCollectionCleanup(viewModel.RootObject);

        sb.AppendLine(i + "}");
        sb.AppendLine();

        sb.AppendLine(i + "private static void __WXSG_ClearCollectionLike(object __value)");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    if (__value is null)");
        sb.AppendLine(i + "    {");
        sb.AppendLine(i + "        return;");
        sb.AppendLine(i + "    }");
        sb.AppendLine();
        sb.AppendLine(i + "    if (__value is global::System.Collections.IDictionary __dictionary)");
        sb.AppendLine(i + "    {");
        sb.AppendLine(i + "        __dictionary.Clear();");
        sb.AppendLine(i + "        return;");
        sb.AppendLine(i + "    }");
        sb.AppendLine();
        sb.AppendLine(i + "    if (__value is global::System.Collections.IList __list)");
        sb.AppendLine(i + "    {");
        sb.AppendLine(i + "        __list.Clear();");
        sb.AppendLine(i + "        return;");
        sb.AppendLine(i + "    }");
        sb.AppendLine();
        sb.AppendLine(i + "    var __clearMethod = __value.GetType().GetMethod(\"Clear\", global::System.Type.EmptyTypes);");
        sb.AppendLine(i + "    if (__clearMethod is not null)");
        sb.AppendLine(i + "    {");
        sb.AppendLine(i + "        __clearMethod.Invoke(__value, null);");
        sb.AppendLine(i + "    }");
        sb.AppendLine(i + "}");
    }

    private static void EmitMain(GraphEmitter emitter, XamlDocumentModel doc)
    {
        var sb = emitter.Builder;
        var i = emitter.MemberIndent;
        sb.AppendLine(i + "[global::System.STAThreadAttribute()]");
        sb.AppendLine(i + "[global::System.Diagnostics.DebuggerNonUserCode]");
        sb.AppendLine(i + "public static void Main()");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    var app = new global::" + doc.ClassFullName + "();");
        sb.AppendLine(i + "    app.InitializeComponent();");
        sb.AppendLine(i + "    app.Run();");
        sb.AppendLine(i + "}");
    }

    /// <summary>
    /// Finds the <c>StartupUri</c> property assignment on the Application root node and
    /// derives the startup window's fully-qualified type name using the same filename-to-class
    /// convention WPF itself uses: <c>StartupUri="MainWindow.xaml"</c> →
    /// <c>{AppNamespace}.MainWindow</c>.
    ///
    /// Returns <c>null</c> when <c>StartupUri</c> is not set.
    /// </summary>
    private static string? ResolveStartupWindowType(ResolvedViewModel viewModel, XamlDocumentModel doc)
    {
        foreach (var assignment in viewModel.RootObject.PropertyAssignments)
        {
            if (!string.Equals(assignment.PropertyName, "StartupUri", StringComparison.Ordinal))
                continue;

            // Value expression is a quoted string like "\"MainWindow.xaml\""
            if (!TryUnquote(assignment.ValueExpression, out var uriValue))
                uriValue = assignment.ValueExpression.Trim('"');

            if (string.IsNullOrWhiteSpace(uriValue))
                return null;

            // Extract the filename without extension from a potentially path-qualified URI:
            // "MainWindow.xaml"        → "MainWindow"
            // "Views/MainWindow.xaml"  → "MainWindow"
            var fileName = System.IO.Path.GetFileNameWithoutExtension(uriValue.Replace('\\', '/'));
            if (string.IsNullOrWhiteSpace(fileName))
                return null;

            // Prefix with the Application class's namespace (the conventional location for windows).
            var ns = doc.ClassNamespace;
            return string.IsNullOrEmpty(ns) ? fileName : ns + "." + fileName;
        }

        return null;
    }

    private static void EmitOnStartup(GraphEmitter emitter, string startupWindowTypeName)
    {
        var sb = emitter.Builder;
        var i = emitter.MemberIndent;
        var qualifiedType = QualifyType(startupWindowTypeName);
        sb.AppendLine(i + "/// <summary>WPF StartupUri convention: creates and shows the startup window.</summary>");
        sb.AppendLine(i + "[global::System.Diagnostics.DebuggerNonUserCode]");
        sb.AppendLine(i + "protected override void OnStartup(global::System.Windows.StartupEventArgs e)");
        sb.AppendLine(i + "{");
        sb.AppendLine(i + "    base.OnStartup(e);");
        sb.AppendLine(i + "    var __startupWindow = new " + qualifiedType + "();");
        sb.AppendLine(i + "    this.MainWindow = __startupWindow;");
        sb.AppendLine(i + "    __startupWindow.Show();");
        sb.AppendLine(i + "}");
    }

    private static bool IsApplicationDefinition(ResolvedViewModel viewModel)
    {
        var typeName = viewModel.RootObject.TypeName.Replace("global::", string.Empty);
        return string.Equals(typeName, "System.Windows.Application", StringComparison.Ordinal);
    }

    private static string ConvertLiteralExpression(string valueExpression, string? clrPropertyTypeName)
    {
        if (string.IsNullOrWhiteSpace(clrPropertyTypeName))
        {
            return valueExpression;
        }

        var normalizedType = clrPropertyTypeName.Replace("global::", string.Empty).Trim();
        if (normalizedType.Length == 0)
        {
            return valueExpression;
        }

        if (!TryUnquote(valueExpression, out var literalValue))
        {
            return valueExpression;
        }

        if (normalizedType == "string" || normalizedType == "System.String" || normalizedType == "object" || normalizedType == "System.Object")
        {
            return valueExpression;
        }

        if (normalizedType == "bool" || normalizedType == "System.Boolean")
        {
            return bool.TryParse(literalValue, out var boolValue)
                ? (boolValue ? "true" : "false")
                : valueExpression;
        }

        if (normalizedType == "int" || normalizedType == "System.Int32")
        {
            return int.TryParse(literalValue, NumberStyles.Integer, CultureInfo.InvariantCulture, out var intValue)
                ? intValue.ToString(CultureInfo.InvariantCulture)
                : valueExpression;
        }

        if (normalizedType == "long" || normalizedType == "System.Int64")
        {
            return long.TryParse(literalValue, NumberStyles.Integer, CultureInfo.InvariantCulture, out var longValue)
                ? longValue.ToString(CultureInfo.InvariantCulture) + "L"
                : valueExpression;
        }

        if (normalizedType == "double" || normalizedType == "System.Double")
        {
            return double.TryParse(literalValue, NumberStyles.Float, CultureInfo.InvariantCulture, out var doubleValue)
                ? doubleValue.ToString("R", CultureInfo.InvariantCulture)
                : valueExpression;
        }

        if (normalizedType == "float" || normalizedType == "System.Single")
        {
            return float.TryParse(literalValue, NumberStyles.Float, CultureInfo.InvariantCulture, out var floatValue)
                ? floatValue.ToString("R", CultureInfo.InvariantCulture) + "f"
                : valueExpression;
        }

        if (normalizedType == "decimal" || normalizedType == "System.Decimal")
        {
            return decimal.TryParse(literalValue, NumberStyles.Float, CultureInfo.InvariantCulture, out var decimalValue)
                ? decimalValue.ToString(CultureInfo.InvariantCulture) + "m"
                : valueExpression;
        }

        if (normalizedType.EndsWith("?", StringComparison.Ordinal))
        {
            var innerType = normalizedType.Substring(0, normalizedType.Length - 1);
            if (literalValue.Equals("null", StringComparison.OrdinalIgnoreCase))
            {
                return "null";
            }

            return ConvertLiteralExpression(valueExpression, innerType);
        }

        var qualifiedType = QualifyType(normalizedType);
        return "(" + qualifiedType + ")global::System.ComponentModel.TypeDescriptor.GetConverter(typeof(" +
               qualifiedType + ")).ConvertFromInvariantString(" + valueExpression + ")";
    }

    private static bool TryUnquote(string expression, out string literal)
    {
        literal = expression;
        var trimmed = expression.Trim();
        if (trimmed.Length < 2 || trimmed[0] != '"' || trimmed[trimmed.Length - 1] != '"')
        {
            return false;
        }

        var inner = trimmed.Substring(1, trimmed.Length - 2);
        literal = inner.Replace("\\\"", "\"").Replace("\\\\", "\\");
        return true;
    }

    private static string QualifyType(string? typeName)
    {
        if (string.IsNullOrWhiteSpace(typeName))
        {
            return "object";
        }

        return typeName.StartsWith("global::", StringComparison.Ordinal)
            ? typeName
            : "global::" + typeName;
    }

    private sealed class GraphEmitter
    {
        private readonly Dictionary<string, string> _namedFieldTypes;
        private int _localCounter;

        public GraphEmitter(ResolvedViewModel viewModel, StringBuilder builder, string memberIndent)
        {
            ViewModel = viewModel;
            Builder = builder;
            MemberIndent = memberIndent;
            _namedFieldTypes = new Dictionary<string, string>(StringComparer.Ordinal);
            foreach (var namedElement in viewModel.NamedElements)
            {
                if (!_namedFieldTypes.ContainsKey(namedElement.Name))
                {
                    _namedFieldTypes[namedElement.Name] = namedElement.TypeName;
                }
            }
        }

        public ResolvedViewModel ViewModel { get; }

        public StringBuilder Builder { get; }

        public string MemberIndent { get; }

        public void EmitNodeInitialization(ResolvedObjectNode node, string instanceVariable, bool isRootNode)
        {
            EmitNamedFieldAssignment(node, instanceVariable);
            EmitPropertyAssignments(node, instanceVariable);
            EmitPropertyElementAssignments(node, instanceVariable);
            EmitEventSubscriptions(node, instanceVariable);
            EmitChildNodes(node, instanceVariable);

            if (isRootNode && !string.IsNullOrWhiteSpace(node.Name))
            {
                EmitNamedFieldAssignment(node, instanceVariable);
            }
        }

        private void EmitNamedFieldAssignment(ResolvedObjectNode node, string instanceVariable)
        {
            if (string.IsNullOrWhiteSpace(node.Name) || !_namedFieldTypes.TryGetValue(node.Name, out var fieldType))
            {
                return;
            }

            Builder.AppendLine(MemberIndent + "    this." + node.Name + " = (" + QualifyType(fieldType) + ")" + instanceVariable + ";");
        }

        private void EmitPropertyAssignments(ResolvedObjectNode node, string instanceVariable)
        {
            foreach (var assignment in node.PropertyAssignments)
            {
                // StartupUri is handled by an OnStartup override (see ResolveStartupWindowType /
                // EmitOnStartup).  Setting it here would cause Application.DoStartup() to try to
                // load a BAML resource that doesn't exist in Phase 3.
                if (string.Equals(assignment.PropertyName, "StartupUri", StringComparison.Ordinal))
                    continue;

                // {Binding ...} — emit a SetBinding call instead of a direct property assignment.
                if (assignment.ValueKind == ResolvedValueKind.Binding)
                {
                    EmitSetBinding(assignment, instanceVariable);
                    continue;
                }

                var convertedValue = ConvertLiteralExpression(assignment.ValueExpression, assignment.ClrPropertyTypeName);
                var frameworkOwner = assignment.GetFrameworkPropertyOwnerTypeName(WpfFrameworkId);
                if (!string.IsNullOrWhiteSpace(frameworkOwner))
                {
                    Builder.AppendLine(
                        MemberIndent + "    " +
                        QualifyType(frameworkOwner) +
                        ".Set" + assignment.PropertyName + "(" + instanceVariable + ", " + convertedValue + ");");
                    continue;
                }

                Builder.AppendLine(
                    MemberIndent + "    " +
                    instanceVariable + "." + assignment.PropertyName + " = " + convertedValue + ";");
            }
        }

        private void EmitSetBinding(ResolvedPropertyAssignment assignment, string instanceVariable)
        {
            // Re-parse the raw XAML binding expression stored in ValueExpression.
            if (!MarkupParser.TryParseMarkupExtension(assignment.ValueExpression, out var info))
                return;

            // Path: first positional arg OR named "Path" arg.
            string? path = null;
            if (info.PositionalArguments.Length > 0)
                path = info.PositionalArguments[0];
            else
                info.NamedArguments.TryGetValue("Path", out path);

            var pathLiteral = string.IsNullOrEmpty(path)
                ? string.Empty
                : "\"" + path!.Replace("\\", "\\\\").Replace("\"", "\\\"") + "\"";

            // Build the Binding object-initializer for optional named arguments.
            var initParts = new List<string>();
            if (info.NamedArguments.TryGetValue("Mode", out var mode))
                initParts.Add("Mode = global::System.Windows.Data.BindingMode." + mode);
            if (info.NamedArguments.TryGetValue("UpdateSourceTrigger", out var ust))
                initParts.Add("UpdateSourceTrigger = global::System.Windows.Data.UpdateSourceTrigger." + ust);

            var bindingExpr = "new global::System.Windows.Data.Binding(" + pathLiteral + ")";
            if (initParts.Count > 0)
                bindingExpr += " { " + string.Join(", ", initParts) + " }";

            // DependencyProperty field: WPF convention is OwnerType.PropertyNameProperty.
            // For attached properties the framework owner type is stored in FrameworkPayload.
            var ownerTypeName = assignment.GetFrameworkPropertyOwnerTypeName(WpfFrameworkId)
                                ?? assignment.ClrPropertyOwnerTypeName;
            var dpField = QualifyType(ownerTypeName) + "." + assignment.PropertyName + "Property";

            Builder.AppendLine(MemberIndent + "    " + instanceVariable +
                               ".SetBinding(" + dpField + ", " + bindingExpr + ");");
        }

        public void EmitHotReloadCollectionCleanup(ResolvedObjectNode rootNode)
        {
            var emittedMemberAccesses = new HashSet<string>(StringComparer.Ordinal);

            if (!string.IsNullOrWhiteSpace(rootNode.ContentPropertyName))
            {
                var contentPropertyName = rootNode.ContentPropertyName;
                switch (rootNode.ChildAttachmentMode)
                {
                    case ResolvedChildAttachmentMode.Content:
                        Builder.AppendLine(MemberIndent + "    this." + contentPropertyName + " = null;");
                        emittedMemberAccesses.Add("this." + contentPropertyName);
                        break;
                    case ResolvedChildAttachmentMode.ChildrenCollection:
                    case ResolvedChildAttachmentMode.ItemsCollection:
                    case ResolvedChildAttachmentMode.DirectAdd:
                    case ResolvedChildAttachmentMode.DictionaryAdd:
                        Builder.AppendLine(MemberIndent + "    __WXSG_ClearCollectionLike(this." + contentPropertyName + ");");
                        emittedMemberAccesses.Add("this." + contentPropertyName);
                        break;
                }
            }

            foreach (var propertyElement in rootNode.PropertyElementAssignments)
            {
                if (propertyElement.ObjectValues.IsDefaultOrEmpty)
                {
                    continue;
                }

                if (!string.IsNullOrWhiteSpace(propertyElement.GetFrameworkPropertyOwnerTypeName(WpfFrameworkId)))
                {
                    continue;
                }

                var forceResourcesDictionaryAdd =
                    propertyElement.PropertyName.Equals("Resources", StringComparison.Ordinal) &&
                    propertyElement.ObjectValues.Length > 0;

                if (!(propertyElement.IsCollectionAdd ||
                      IsCollectionLikeTypeName(propertyElement.ClrPropertyTypeName) ||
                      forceResourcesDictionaryAdd))
                {
                    continue;
                }

                var memberAccess = "this." + propertyElement.PropertyName;
                if (!emittedMemberAccesses.Add(memberAccess))
                {
                    continue;
                }

                Builder.AppendLine(MemberIndent + "    __WXSG_ClearCollectionLike(" + memberAccess + ");");
            }
        }

        private void EmitPropertyElementAssignments(ResolvedObjectNode node, string instanceVariable)
        {
            foreach (var propertyElement in node.PropertyElementAssignments)
            {
                if (propertyElement.ObjectValues.IsDefaultOrEmpty)
                {
                    continue;
                }

                var createdValues = new List<string>(propertyElement.ObjectValues.Length);
                foreach (var objectValue in propertyElement.ObjectValues)
                {
                    createdValues.Add(EmitChildObjectCreation(objectValue));
                }

                var frameworkOwner = propertyElement.GetFrameworkPropertyOwnerTypeName(WpfFrameworkId);
                if (!string.IsNullOrWhiteSpace(frameworkOwner))
                {
                    foreach (var childVariable in createdValues)
                    {
                        Builder.AppendLine(
                            MemberIndent + "    " +
                            QualifyType(frameworkOwner) +
                            ".Set" + propertyElement.PropertyName + "(" + instanceVariable + ", " + childVariable + ");");
                    }

                    continue;
                }

                var forceResourcesDictionaryAdd =
                    propertyElement.PropertyName.Equals("Resources", StringComparison.Ordinal) &&
                    propertyElement.ObjectValues.Length > 0;

                if (propertyElement.IsCollectionAdd ||
                    IsCollectionLikeTypeName(propertyElement.ClrPropertyTypeName) ||
                    forceResourcesDictionaryAdd)
                {
                    var isDictionaryAdd = forceResourcesDictionaryAdd ||
                                          IsDictionaryLikeTypeName(propertyElement.ClrPropertyTypeName);
                    for (var index = 0; index < createdValues.Count; index++)
                    {
                        var childVariable = createdValues[index];
                        if (isDictionaryAdd)
                        {
                            var keyExpression = propertyElement.ObjectValues[index].KeyExpression ??
                                                AsFallbackDictionaryKey(propertyElement.ObjectValues[index]);
                            Builder.AppendLine(
                                MemberIndent + "    " +
                                instanceVariable + "." + propertyElement.PropertyName + ".Add(" +
                                keyExpression + ", " + childVariable + ");");
                        }
                        else
                        {
                            Builder.AppendLine(
                                MemberIndent + "    " +
                                instanceVariable + "." + propertyElement.PropertyName + ".Add(" + childVariable + ");");
                        }
                    }

                    continue;
                }

                Builder.AppendLine(
                    MemberIndent + "    " +
                    instanceVariable + "." + propertyElement.PropertyName + " = " + createdValues[0] + ";");
            }
        }

        private void EmitEventSubscriptions(ResolvedObjectNode node, string instanceVariable)
        {
            foreach (var subscription in node.EventSubscriptions)
            {
                if (string.IsNullOrWhiteSpace(subscription.HandlerMethodName))
                {
                    continue;
                }

                if (subscription.Kind == ResolvedEventSubscriptionKind.RoutedEvent &&
                    !string.IsNullOrWhiteSpace(subscription.RoutedEventOwnerTypeName) &&
                    !string.IsNullOrWhiteSpace(subscription.RoutedEventFieldName))
                {
                    Builder.AppendLine(
                        MemberIndent + "    " +
                        instanceVariable +
                        ".AddHandler(" +
                        QualifyType(subscription.RoutedEventOwnerTypeName) +
                        "." + subscription.RoutedEventFieldName +
                        ", new global::System.Windows.RoutedEventHandler(this." +
                        subscription.HandlerMethodName + "));" );
                    continue;
                }

                Builder.AppendLine(
                    MemberIndent + "    " +
                    instanceVariable +
                    "." + subscription.EventName +
                    " += this." + subscription.HandlerMethodName + ";");
            }
        }

        private void EmitChildNodes(ResolvedObjectNode node, string instanceVariable)
        {
            if (node.Children.IsDefaultOrEmpty)
            {
                return;
            }

            foreach (var child in node.Children)
            {
                var childVariable = EmitChildObjectCreation(child);
                AttachChild(node, instanceVariable, child, childVariable);
            }
        }

        private string EmitChildObjectCreation(ResolvedObjectNode child)
        {
            var localVariable = "__node" + _localCounter.ToString(CultureInfo.InvariantCulture);
            _localCounter++;

            var creationExpression = !string.IsNullOrWhiteSpace(child.FactoryExpression)
                ? child.FactoryExpression
                : "new " + QualifyType(child.TypeName) + "()";

            Builder.AppendLine(MemberIndent + "    var " + localVariable + " = " + creationExpression + ";");
            EmitNodeInitialization(child, localVariable, isRootNode: false);
            return localVariable;
        }

        private void AttachChild(
            ResolvedObjectNode parent,
            string parentVariable,
            ResolvedObjectNode child,
            string childVariable)
        {
            var contentProperty = parent.ContentPropertyName;
            if (string.IsNullOrWhiteSpace(contentProperty))
            {
                return;
            }

            switch (parent.ChildAttachmentMode)
            {
                case ResolvedChildAttachmentMode.Content:
                    Builder.AppendLine(
                        MemberIndent + "    " +
                        parentVariable + "." + contentProperty + " = " + childVariable + ";");
                    return;
                case ResolvedChildAttachmentMode.ChildrenCollection:
                case ResolvedChildAttachmentMode.ItemsCollection:
                case ResolvedChildAttachmentMode.DirectAdd:
                    Builder.AppendLine(
                        MemberIndent + "    " +
                        parentVariable + "." + contentProperty + ".Add(" + childVariable + ");");
                    return;
                case ResolvedChildAttachmentMode.DictionaryAdd:
                    var keyExpression = child.KeyExpression ?? AsFallbackDictionaryKey(child);
                    Builder.AppendLine(
                        MemberIndent + "    " +
                        parentVariable + "." + contentProperty + ".Add(" + keyExpression + ", " + childVariable + ");");
                    return;
                default:
                    return;
            }
        }

        private static bool IsCollectionLikeTypeName(string? typeName)
        {
            if (string.IsNullOrWhiteSpace(typeName))
            {
                return false;
            }

            var normalized = typeName.Replace("global::", string.Empty);
            return normalized.Contains("ICollection", StringComparison.Ordinal) ||
                   normalized.Contains("IList", StringComparison.Ordinal) ||
                   normalized.Contains("IEnumerable", StringComparison.Ordinal) ||
                   normalized.Contains("Collection", StringComparison.Ordinal) ||
                   normalized.Contains("List", StringComparison.Ordinal);
        }

        private static bool IsDictionaryLikeTypeName(string? typeName)
        {
            if (string.IsNullOrWhiteSpace(typeName))
            {
                return false;
            }

            var normalized = typeName.Replace("global::", string.Empty);
            return normalized.Contains("IDictionary", StringComparison.Ordinal) ||
                   normalized.Contains("Dictionary", StringComparison.Ordinal);
        }

        private static string AsFallbackDictionaryKey(ResolvedObjectNode child)
        {
            if (!string.IsNullOrWhiteSpace(child.Name))
            {
                return "\"" + child.Name + "\"";
            }

            return "\"" + child.TypeName.Replace("\"", "\\\"") + "\"";
        }
    }
